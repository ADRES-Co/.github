-- Tabla año 2018, NUM_ANO, STR_COD_EPS, DESC_EPS, STR_REGIMEN, STR_COD_ACTIVIDAD,  NOMBRE_ACTIVIDAD, NT_RULES, NT_DENOMINA, DESC_ACTIVIDAD, Suma_total_valor, Frecuencia
CREATE OR REPLACE TABLE achavez_tabla_muestra_cuatro_millon_suficiencia_2018
AS
SELECT
  TO_DATE(CAST(NUM_ANO AS STRING), 'yyyy') AS Year,
  STR_COD_EPS, 
  DESC_EPS, 
  STR_REGIMEN, 
  STR_COD_ACTIVIDAD,  
  NOMBRE_ACTIVIDAD, 
  DESC_ACTIVIDAD,
  NT_RULES, 
  NT_DENOMINA, 
  SUM(NUM_VALOR_TOTAL) AS Suma_total_valor,
  COUNT(NT_RULES) AS Frecuencia
FROM achavez_muestra_cuatro_millon_suficiencia_2018
GROUP BY TO_DATE(CAST(NUM_ANO AS STRING), 'yyyy'), STR_COD_EPS, 
  DESC_EPS, 
  STR_REGIMEN, 
  STR_COD_ACTIVIDAD,  
  NOMBRE_ACTIVIDAD, 
  DESC_ACTIVIDAD,
  NT_RULES, 
  NT_DENOMINA;

SELECT
  *
FROM
  achavez_tabla_muestra_cuatro_millon_suficiencia_2018

